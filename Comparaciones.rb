# Se pueden hacer comparaciones entre diferentes elemetos
# Boolean
puts 10 > 1
# Igualdad
puts "cat" == "dog"
# Opuesto
puts 1 != 2

# Existe precedencia entre los operadores lógicos
# En este ejemplo se evalúa primero el operador &&
puts false || true && false
puts true || true && false

